# Integer
age = 25
print("Age:", age)
print("Type:", type(age))

# Float
salary = 55000.75
print("\nSalary:", salary)
print("Type:", type(salary))

# String
name = "Shabana"
print("\nName:", name)
print("Type:", type(name))

# Boolean
is_working = True
print("\nIs Working:", is_working)
print("Type:", type(is_working))

# List
skills = ["Python", "React", "FastAPI"]
print("\nSkills:", skills)
print("Type:", type(skills))

# Tuple
numbers = (1, 2, 3)
print("\nNumbers:", numbers)
print("Type:", type(numbers))

# Dictionary
person = {"name": "Shabana", "age": 25}
print("\nPerson:", person)
print("Type:", type(person))

# Set
unique_numbers = {1, 2, 3, 3}
print("\nUnique Numbers:", unique_numbers)
print("Type:", type(unique_numbers))